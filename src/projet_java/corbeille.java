    private void delet_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delet_btnActionPerformed
        
        int ligne = list_deleted.getSelectedRow();
        String clientId = String.valueOf(list_deleted.getValueAt(ligne, 0));
        System.out.println("client id : " + clientId);
        if (clientId==null){
            JOptionPane.showMessageDialog(this, "Veuillez choisir un svp");
        }
        else{
            int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer ce personnel ?", "Supprimer definitivement", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response==JOptionPane.YES_OPTION){
                try {
                    controlPersonne cp = new controlPersonne();
                    cp.delete(clientId);
                    ChargerListe();

                } catch (Exception ex) {
                    Logger.getLogger(corbeille.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
        }
    }//GEN-LAST:event_delet_btnActionPerformed

