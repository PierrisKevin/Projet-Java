// fucntion to charge all liste of personnal
    public void ChargerListe() {
        String titre[] = {"id", "Nom", "Pr√©noms","Date de naissance","Adresse", "Numero de telephone", "CIN"};
        Object enreg[][] = new Object[1][1];

        try {
            controlPersonne cp = new controlPersonne();
            int nbr = cp.countEnreg();
            enreg = new Object[nbr][titre.length];
            ResultSet rs1 = cp.getAllDel();
            int i = 0;
            while (rs1.next()) {
                enreg[i][0] = (Object) rs1.getString("id");
                enreg[i][1] = (Object) rs1.getString("nom");
                enreg[i][2] = (Object) rs1.getString("prenom");
                enreg[i][3] = (Object) rs1.getString("Date_naissance");
                enreg[i][4] = (Object) rs1.getString("adresse");
                enreg[i][5] = (Object) rs1.getString("num_tel");
                enreg[i][6] = (Object) rs1.getString("Num_cin");
                i++;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        list_deleted.setModel(new DefaultTableModel(enreg, titre));
        delet_contain.setViewportView(list_deleted);
    }
    
